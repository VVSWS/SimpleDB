package ru.tusur.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey

// ---------------------------------------------------------
// Сущность Room для таблицы марок автомобилей
// ---------------------------------------------------------
// Хранит справочную информацию о производителях техники
// Имя марки используется в качестве первичного ключа (уникальное значение)
@Entity(tableName = "brands")
data class BrandEntity(
    // ---------------------------------------------------------
    // Первичный ключ: название марки (например, "Toyota", "BMW", "Lada")
    // ---------------------------------------------------------
    // Используется как естественный ключ, так как названия марок уникальны
    // Не требуется отдельный числовой ID
    @PrimaryKey val name: String
)