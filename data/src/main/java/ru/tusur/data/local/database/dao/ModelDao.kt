package ru.tusur.data.local.database.dao

import androidx.room.*
import kotlinx.coroutines.flow.Flow
import ru.tusur.data.local.entity.ModelEntity

// ---------------------------------------------------------
// DAO для работы со справочником моделей автомобилей
// ---------------------------------------------------------
// Обеспечивает доступ к таблице "models" в базе данных Room
// Модель привязана к марке (brandName) и году выпуска (yearValue)
@Dao
interface ModelDao {

    // ---------------------------------------------------------
    // Вставка или обновление модели (для использования в UI)
    // ---------------------------------------------------------
    // При конфликте первичного ключа (одинаковый ID) - замена существующей записи
    // Возвращает ID вставленной или обновлённой записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertModel(entity: ModelEntity): Long

    // ---------------------------------------------------------
    // Вставка модели только если она отсутствует (для импорта/слияния)
    // ---------------------------------------------------------
    // При конфликте первичного ключа - операция игнорируется
    // Полезно при импорте данных, чтобы не создавать дубликаты
    @Insert(onConflict = OnConflictStrategy.IGNORE)
    suspend fun insertIfMissing(entity: ModelEntity)

    // ---------------------------------------------------------
    // Удаление модели из справочника
    // ---------------------------------------------------------
    // Удаляет запись по переданному Entity (сравнивает по первичному ключу)
    @Delete
    suspend fun deleteModel(entity: ModelEntity)

    // ---------------------------------------------------------
    // Получение моделей, отфильтрованных по марке и году (реактивный поток)
    // ---------------------------------------------------------
    // Возвращает Flow, который автоматически обновляет подписчиков
    // при изменении данных в таблице "models"
    // Используется для динамического обновления списка моделей
    // при выборе марки и года в форме создания записи
    @Query("""
        SELECT * FROM models
        WHERE brandName = :brandName
          AND yearValue = :yearValue
        ORDER BY name ASC
    """)
    fun getModelsForBrandAndYear(
        brandName: String,
        yearValue: Int
    ): Flow<List<ModelEntity>>

    // ---------------------------------------------------------
    // Получение всех моделей в виде реактивного потока
    // ---------------------------------------------------------
    // Возвращает Flow со списком всех моделей, отсортированных по имени
    // Используется в административных экранах для управления справочником
    @Query("SELECT * FROM models ORDER BY name ASC")
    fun getAllModels(): Flow<List<ModelEntity>>
}