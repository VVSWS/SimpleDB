package ru.tusur.data.local.database.dao

import androidx.room.*
import kotlinx.coroutines.flow.Flow
import ru.tusur.data.local.entity.BrandEntity

// ---------------------------------------------------------
// DAO для работы со справочником марок автомобилей
// ---------------------------------------------------------
// Обеспечивает доступ к таблице "brands" в базе данных Room
// Поддерживает операции вставки, удаления и получения всех марок
@Dao
interface BrandDao {

    // ---------------------------------------------------------
    // Вставка или обновление марки (для использования в UI)
    // ---------------------------------------------------------
    // При конфликте первичного ключа (одинаковый ID) - замена существующей записи
    // Возвращает ID вставленной или обновлённой записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertBrand(entity: BrandEntity): Long

    // ---------------------------------------------------------
    // Вставка марки только если она отсутствует (для импорта/слияния)
    // ---------------------------------------------------------
    // При конфликте первичного ключа - операция игнорируется
    // Полезно при импорте данных, чтобы не создавать дубликаты
    @Insert(onConflict = OnConflictStrategy.IGNORE)
    suspend fun insertIfMissing(entity: BrandEntity)

    // ---------------------------------------------------------
    // Удаление марки из справочника
    // ---------------------------------------------------------
    // Удаляет запись по переданному Entity (сравнивает по первичному ключу)
    @Delete
    suspend fun deleteBrand(entity: BrandEntity)

    // ---------------------------------------------------------
    // Получение всех марок в виде реактивного потока
    // ---------------------------------------------------------
    // Возвращает Flow, который автоматически обновляет подписчиков
    // при изменении данных в таблице "brands"
    // Сортировка по имени марки в алфавитном порядке (A → Я)
    @Query("SELECT * FROM brands ORDER BY name ASC")
    fun getAllBrands(): Flow<List<BrandEntity>>
}