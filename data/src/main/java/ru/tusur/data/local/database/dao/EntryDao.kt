package ru.tusur.data.local.database.dao

import androidx.room.*
import kotlinx.coroutines.flow.Flow
import ru.tusur.data.local.entity.EntryEntity
import ru.tusur.data.local.entity.EntryWithImages
import ru.tusur.data.local.entity.EntryWithRelations
import ru.tusur.data.local.entity.ModelEntity

// ---------------------------------------------------------
// DAO для операций с основными записями об отказах техники
// ---------------------------------------------------------
// Обеспечивает полный CRUD для записей о неисправностях
// Поддерживает фильтрацию, поиск, транзакционную загрузку связанных данных
@Dao
interface EntryDao {

    // ---------------------------------------------------------
    // Получение всех записей с полными связями (реактивный поток)
    // ---------------------------------------------------------
    // Транзакционный запрос с загрузкой связанных справочников (год, марка, модель, локация)
    // Автоматически обновляет UI при изменении данных
    @Transaction
    @Query("SELECT * FROM entries ORDER BY timestamp DESC")
    fun getAllEntries(): Flow<List<EntryWithRelations>>

    // ---------------------------------------------------------
    // Получение последних записей (ограниченное количество)
    // ---------------------------------------------------------
    // Выборка 7 самых свежих записей по временной метке
    // Используется на главном экране для отображения недавней активности
    @Query("SELECT * FROM entries ORDER BY timestamp DESC LIMIT 7")
    suspend fun getRecentEntries(): List<EntryEntity>

    // ---------------------------------------------------------
    // Синхронное получение всех записей (без реактивности)
    // ---------------------------------------------------------
    // Прямой доступ к данным без потока Flow (для фоновых операций)
    // Используется в экспорте/импорте данных
    @Query("SELECT * FROM entries")
    fun getAllSync(): List<EntryEntity>

    // ---------------------------------------------------------
    // Получение записи с изображениями по идентификатору
    // ---------------------------------------------------------
    // Транзакционная загрузка записи вместе со связанными изображениями
    // Возвращает null, если запись не найдена
    @Transaction
    @Query("SELECT * FROM entries WHERE id = :id")
    suspend fun getEntryById(id: Long): EntryWithImages?

    // ---------------------------------------------------------
    // Получение простой сущности записи по идентификатору
    // ---------------------------------------------------------
    // Быстрая загрузка без связанных данных (изображений, справочников)
    // Используется для операций обновления, где связи не нужны
    @Query("SELECT * FROM entries WHERE id = :id")
    suspend fun getById(id: Long): EntryEntity?

    // ---------------------------------------------------------
    // Получение записи для экрана аудиозаписи
    // ---------------------------------------------------------
    // Транзакционная загрузка записи с изображениями для проигрывателя
    // Гарантирует наличие данных для воспроизведения аудио
    @Transaction
    @Query("SELECT * FROM entries WHERE id = :id")
    suspend fun getEntryWithRecording(id: Long): EntryWithImages

    // ---------------------------------------------------------
    // Поиск записей по фильтрам
    // ---------------------------------------------------------
    // Динамическая фильтрация по году, марке, модели и местоположению
    // NULL в параметре означает "не фильтровать по этому полю"
    @Query("""
    SELECT * FROM entries
    WHERE (:year IS NULL OR year = :year)
      AND (:brand IS NULL OR brand = :brand)
      AND (:modelName IS NULL OR modelName = :modelName)
      AND (:location IS NULL OR location = :location)
    ORDER BY timestamp DESC
""")
    suspend fun searchEntries(
        year: Int?,
        brand: String?,
        modelName: String?,
        location: String?
    ): List<EntryEntity>

    // ---------------------------------------------------------
    // Создание новой записи
    // ---------------------------------------------------------
    // Вставка записи с заменой при конфликте первичного ключа
    // Возвращает сгенерированный ID записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertEntry(entry: EntryEntity): Long

    // ---------------------------------------------------------
    // Обновление существующей записи
    // ---------------------------------------------------------
    // Обновление записи по первичному ключу
    // Если записи нет - ничего не происходит
    @Update
    suspend fun updateEntry(entry: EntryEntity)

    // ---------------------------------------------------------
    // Удаление записи через объект
    // ---------------------------------------------------------
    // Удаление записи с использованием объекта сущности
    // Сравнивает по первичному ключу
    @Delete
    suspend fun deleteEntry(entry: EntryEntity)

    // ---------------------------------------------------------
    // Получение общего количества записей в таблице
    // ---------------------------------------------------------
    // Используется для статистики и отображения размера базы данных
    @Query("SELECT COUNT(*) FROM entries")
    suspend fun getEntryCount(): Int

    // ---------------------------------------------------------
    // Получение списка всех идентификаторов записей
    // ---------------------------------------------------------
    // Используется для пакетных операций и миграций данных
    @Query("SELECT id FROM entries")
    suspend fun getAllIds(): List<Long>

    // ---------------------------------------------------------
    // Получение записей с изображениями (полная выборка)
    // ---------------------------------------------------------
    // Транзакционная загрузка всех записей вместе со списками их изображений
    // Используется при экспорте базы данных
    @Transaction
    @Query("SELECT * FROM entries ORDER BY timestamp DESC")
    suspend fun getAllEntriesWithImages(): List<EntryWithImages>

    // ---------------------------------------------------------
    // Поиск модели техники по составному ключу
    // ---------------------------------------------------------
    // Поиск связанной модели по трём полям (имя, марка, год) для денормализации
    // Используется для проверки существования модели перед вставкой
    @Query("""
    SELECT * FROM models
    WHERE name = :modelName
      AND brandName = :modelBrand
      AND yearValue = :modelYear
    LIMIT 1
""")
    suspend fun getModelForEntry(
        modelName: String?,
        modelBrand: String?,
        modelYear: Int?
    ): ModelEntity?

    // ---------------------------------------------------------
    // Удаление записи по идентификатору
    // ---------------------------------------------------------
    // Прямое удаление записи по ID (используется в транзакциях)
    // Более эффективно, чем удаление через объект
    @Query("DELETE FROM entries WHERE id = :id")
    suspend fun deleteEntryById(id: Long)

    // ---------------------------------------------------------
    // Массовое удаление всех записей
    // ---------------------------------------------------------
    // Полная очистка таблицы записей (для сброса базы данных)
    // Используется при переключении между базами данных
    @Query("DELETE FROM entries")
    suspend fun deleteAllEntries()
}