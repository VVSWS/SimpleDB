package ru.tusur.data.local.entity

import androidx.room.Entity
import androidx.room.ForeignKey
import androidx.room.Index
import androidx.room.PrimaryKey

// ---------------------------------------------------------
// Сущность Room для связующей таблицы "записи-изображения"
// ---------------------------------------------------------
// Реализует отношение "многие-ко-многим" между записями и изображениями
// Одна запись может иметь несколько изображений
// Одно изображение может принадлежать нескольким записям (если скопировано)
@Entity(
    tableName = "entry_images",
    foreignKeys = [
        // ---------------------------------------------------------
        // Внешний ключ: связь с таблицей записей
        // ---------------------------------------------------------
        ForeignKey(
            entity = EntryEntity::class,          // Родительская таблица
            parentColumns = ["id"],                // Поле в родительской таблице (первичный ключ)
            childColumns = ["entryId"],            // Поле в дочерней таблице
            onDelete = ForeignKey.CASCADE          // При удалении записи - удалить все связи с изображениями
        )
    ],
    indices = [
        // ---------------------------------------------------------
        // Индекс для ускорения запросов по entryId
        // ---------------------------------------------------------
        // Оптимизирует выборку всех изображений для конкретной записи
        Index("entryId")
    ]
)
data class EntryImageEntity(
    // ---------------------------------------------------------
    // Первичный ключ с автогенерацией
    // ---------------------------------------------------------
    @PrimaryKey(autoGenerate = true)
    val id: Long = 0,

    // ---------------------------------------------------------
    // Внешний ключ: ID записи
    // ---------------------------------------------------------
    val entryId: Long,

    // ---------------------------------------------------------
    // URI изображения (может быть content://, file:// или относительный путь)
    // ---------------------------------------------------------
    val uri: String
)