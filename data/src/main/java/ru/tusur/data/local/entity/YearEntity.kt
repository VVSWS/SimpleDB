package ru.tusur.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey

// ---------------------------------------------------------
// Сущность справочника годов выпуска техники
// ---------------------------------------------------------
// Хранит список доступных годов выпуска автомобилей
// Используется в формах создания записи для выбора года
@Entity(tableName = "years")
data class YearEntity(
    // ---------------------------------------------------------
    // Первичный ключ справочника
    // ---------------------------------------------------------
    // Значение года (например, 2020, 2021, 2022) используется как уникальный идентификатор
    // Не требуется отдельный числовой ID, так как года уникальны по своей природе
    @PrimaryKey val value: Int
)