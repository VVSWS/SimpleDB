package ru.tusur.data.local.database.dao

import androidx.room.*
import ru.tusur.data.local.entity.EntryImageEntity

// ---------------------------------------------------------
// DAO для работы со связями записей и изображений
// ---------------------------------------------------------
// Управляет таблицей "entry_images" (многие-ко-многим)
// Обеспечивает привязку изображений к конкретным записям
@Dao
interface EntryImageDao {

    // ---------------------------------------------------------
    // Получение всех изображений для конкретной записи
    // ---------------------------------------------------------
    // Возвращает список Entity с URI изображений, связанных с entryId
    @Query("SELECT * FROM entry_images WHERE entryId = :entryId")
    suspend fun getImagesForEntry(entryId: Long): List<EntryImageEntity>

    // ---------------------------------------------------------
    // Вставка одного изображения
    // ---------------------------------------------------------
    // При конфликте (одинаковый entryId + uri) - замена существующей записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertImage(image: EntryImageEntity)

    // ---------------------------------------------------------
    // Массовая вставка нескольких изображений
    // ---------------------------------------------------------
    // Эффективнее, чем вызывать insertImage в цикле
    // Используется при импорте данных
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertImages(images: List<EntryImageEntity>)

    // ---------------------------------------------------------
    // Удаление всех изображений для конкретной записи
    // ---------------------------------------------------------
    // Вызывается перед удалением самой записи или при очистке её изображений
    @Query("DELETE FROM entry_images WHERE entryId = :entryId")
    suspend fun deleteImagesForEntry(entryId: Long)

    // ---------------------------------------------------------
    // Удаление конкретного изображения у записи
    // ---------------------------------------------------------
    // Удаляет связь между записью и изображением по URI
    // Не удаляет сам физический файл изображения
    @Query("DELETE FROM entry_images WHERE entryId = :entryId AND uri = :uri")
    suspend fun deleteImage(entryId: Long, uri: String)

    // ---------------------------------------------------------
    // Удаление всех связей с определённым URI
    // ---------------------------------------------------------
    // Используется при удалении физического файла изображения
    // Чтобы удалить все ссылки на него из разных записей
    @Query("DELETE FROM entry_images WHERE uri = :uri")
    suspend fun deleteImageByUri(uri: String)

    // ---------------------------------------------------------
    // Полная очистка таблицы связей
    // ---------------------------------------------------------
    // Удаляет все связи между записями и изображениями
    // Используется при сбросе базы данных
    @Query("DELETE FROM entry_images")
    suspend fun deleteAllImages()
}