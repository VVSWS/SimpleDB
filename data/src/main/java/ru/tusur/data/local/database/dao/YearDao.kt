package ru.tusur.data.local.database.dao

import androidx.room.*
import kotlinx.coroutines.flow.Flow
import ru.tusur.data.local.entity.YearEntity

// ---------------------------------------------------------
// DAO для работы со справочником годов выпуска техники
// ---------------------------------------------------------
// Обеспечивает доступ к таблице "years" в базе данных Room
// Хранит список доступных годов выпуска автомобилей
@Dao
interface YearDao {

    // ---------------------------------------------------------
    // Вставка или обновление года (для использования в UI)
    // ---------------------------------------------------------
    // Замена существующей записи при конфликте первичного ключа
    // Возвращает ID вставленной или обновлённой записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertYear(entity: YearEntity): Long

    // ---------------------------------------------------------
    // Вставка года только если он отсутствует (для импорта/слияния)
    // ---------------------------------------------------------
    // Игнорирование операции при конфликте первичного ключа
    // Полезно при импорте данных, чтобы не создавать дубликаты
    @Insert(onConflict = OnConflictStrategy.IGNORE)
    suspend fun insertIfMissing(entity: YearEntity)

    // ---------------------------------------------------------
    // Удаление года из справочника
    // ---------------------------------------------------------
    // Удаление записи по значению первичного ключа
    @Delete
    suspend fun deleteYear(entity: YearEntity)

    // ---------------------------------------------------------
    // Получение всех годов в виде реактивного потока
    // ---------------------------------------------------------
    // Возврат потока со списком годов, отсортированных по возрастанию (от старых к новым)
    // Flow автоматически обновляет подписчиков при изменении данных
    @Query("SELECT * FROM years ORDER BY value ASC")
    fun getAllYears(): Flow<List<YearEntity>>
}