package ru.tusur.data.local.entity

import androidx.room.Entity

// ---------------------------------------------------------
// Сущность Room для таблицы моделей автомобилей
// ---------------------------------------------------------
// Хранит справочную информацию о моделях техники
// Модель уникальна в рамках конкретной марки и года выпуска
// Одна марка может иметь несколько моделей в один и тот же год
@Entity(
    tableName = "models",
    // ---------------------------------------------------------
    // Составной первичный ключ (три поля вместе обеспечивают уникальность)
    // ---------------------------------------------------------
    // name - название модели (например, "Camry")
    // brandName - марка автомобиля (например, "Toyota")
    // yearValue - год выпуска (например, 2020)
    // Одна и та же модель "Camry" может существовать у Toyota в разные годы
    primaryKeys = ["name", "brandName", "yearValue"]
)
data class ModelEntity(
    // Название модели (например, "X5", "A4", "Granta")
    val name: String,

    // Марка, к которой относится модель (внешний ключ к BrandEntity)
    val brandName: String,

    // Год выпуска, для которого доступна модель (внешний ключ к YearEntity)
    val yearValue: Int
)