package ru.tusur.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey

// ---------------------------------------------------------
// Сущность Room для таблицы местоположений
// ---------------------------------------------------------
// Хранит справочную информацию о местах возникновения неисправностей
// Название локации используется в качестве первичного ключа (уникальное значение)
@Entity(tableName = "locations")
data class LocationEntity(
    // ---------------------------------------------------------
    // Первичный ключ: название местоположения (например, "Двигатель", "Ходовая", "Электрика")
    // ---------------------------------------------------------
    // Используется как естественный ключ, так как названия локаций уникальны
    // Не требуется отдельный числовой ID
    @PrimaryKey val name: String
)