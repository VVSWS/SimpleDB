package ru.tusur.data.local.database.dao

import androidx.room.*
import kotlinx.coroutines.flow.Flow
import ru.tusur.data.local.entity.LocationEntity

// ---------------------------------------------------------
// DAO для работы со справочником местоположений
// ---------------------------------------------------------
// Обеспечивает доступ к таблице "locations" в базе данных Room
// Используется для выбора места возникновения неисправности
@Dao
interface LocationDao {

    // ---------------------------------------------------------
    // Вставка или обновление местоположения (для использования в UI)
    // ---------------------------------------------------------
    // При конфликте первичного ключа (одинаковый ID) - замена существующей записи
    // Возвращает ID вставленной или обновлённой записи
    @Insert(onConflict = OnConflictStrategy.REPLACE)
    suspend fun insertLocation(entity: LocationEntity): Long

    // ---------------------------------------------------------
    // Вставка местоположения только если оно отсутствует (для импорта/слияния)
    // ---------------------------------------------------------
    // При конфликте первичного ключа - операция игнорируется
    // Полезно при импорте данных, чтобы не создавать дубликаты
    @Insert(onConflict = OnConflictStrategy.IGNORE)
    suspend fun insertIfMissing(entity: LocationEntity)

    // ---------------------------------------------------------
    // Удаление местоположения из справочника
    // ---------------------------------------------------------
    // Удаляет запись по переданному Entity (сравнивает по первичному ключу)
    @Delete
    suspend fun deleteLocation(entity: LocationEntity)

    // ---------------------------------------------------------
    // Получение всех местоположений в виде реактивного потока
    // ---------------------------------------------------------
    // Возвращает Flow, который автоматически обновляет подписчиков
    // при изменении данных в таблице "locations"
    // Сортировка по имени местоположения в алфавитном порядке (А → Я)
    @Query("SELECT * FROM locations ORDER BY name ASC")
    fun getAllLocations(): Flow<List<LocationEntity>>
}