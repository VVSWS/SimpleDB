package ru.tusur.domain.usecase.entry

import ru.tusur.domain.repository.FaultRepository

// ---------------------------------------------------------
// UseCase для получения последних записей о неисправностях
// ---------------------------------------------------------
// Инкапсулирует бизнес-логику получения ограниченного количества
// самых свежих записей (по временной метке)
// Используется на главном экране для отображения недавней активности
class GetRecentEntriesUseCase(
    private val repository: FaultRepository   // Репозиторий для работы с записями
) {
    // ---------------------------------------------------------
    // Оператор invoke - выполнение получения последних записей
    // ---------------------------------------------------------
    // limit: максимальное количество записей для возврата (по умолчанию 20)
    // Возвращает: List<FaultEntry> - список последних записей
    suspend operator fun invoke(limit: Int = 20) = repository.getRecentEntries(limit)
}