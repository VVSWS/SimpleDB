package ru.tusur.domain.usecase.entry

import ru.tusur.domain.repository.FaultRepository

// ---------------------------------------------------------
// UseCase для получения всех записей о неисправностях
// ---------------------------------------------------------
// Инкапсулирует бизнес-логику получения всех записей
// Возвращает реактивный поток (Flow), который автоматически обновляется
// при изменениях в базе данных
class GetEntriesUseCase(
    private val repository: FaultRepository   // Репозиторий для работы с записями
) {
    // ---------------------------------------------------------
    // Оператор invoke - выполнение получения всех записей
    // ---------------------------------------------------------
    // Возвращает: Flow<List<FaultEntry>> - реактивный поток со списком всех записей
    operator fun invoke() = repository.getAllEntries()
}