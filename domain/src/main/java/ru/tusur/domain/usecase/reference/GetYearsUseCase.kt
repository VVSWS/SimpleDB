package ru.tusur.domain.usecase.reference

import ru.tusur.domain.repository.ReferenceDataRepository

// ---------------------------------------------------------
// UseCase для получения списка всех годов выпуска
// ---------------------------------------------------------
// Инкапсулирует бизнес-логику получения справочника годов
// Возвращает реактивный поток (Flow), который автоматически обновляется
// при изменениях в справочнике годов
// Используется в форме создания записи для выбора года выпуска автомобиля
class GetYearsUseCase(
    private val repository: ReferenceDataRepository   // Репозиторий для работы со справочниками
) {
    // ---------------------------------------------------------
    // Оператор invoke - выполнение получения списка годов
    // ---------------------------------------------------------
    // Возвращает: Flow<List<Year>> - реактивный поток со списком всех годов,
    // отсортированных по возрастанию
    operator fun invoke() = repository.getYears()
}