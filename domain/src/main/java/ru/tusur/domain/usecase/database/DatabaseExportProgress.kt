package ru.tusur.domain.usecase.database

// ---------------------------------------------------------
// Sealed class для отслеживания прогресса экспорта базы данных
// ---------------------------------------------------------
// Используется для передачи информации о ходе экспорта из UseCase в UI
// Позволяет отображать прогресс-бар и сообщать об ошибках
sealed class DatabaseExportProgress {

    // ---------------------------------------------------------
    // Состояние: экспорт начат
    // ---------------------------------------------------------
    // Содержит общий размер всех данных для экспорта (в байтах)
    // Используется для инициализации прогресс-бара
    data class Started(val totalBytes: Long) : DatabaseExportProgress()

    // ---------------------------------------------------------
    // Состояние: промежуточный прогресс
    // ---------------------------------------------------------
    // Содержит количество уже записанных байт и общий размер
    // Используется для обновления прогресс-бара
    data class Progress(val writtenBytes: Long, val totalBytes: Long) : DatabaseExportProgress()

    // ---------------------------------------------------------
    // Состояние: экспорт успешно завершён
    // ---------------------------------------------------------
    // Сигнализирует UI о необходимости закрыть диалог прогресса
    object Finished : DatabaseExportProgress()

    // ---------------------------------------------------------
    // Состояние: ошибка при экспорте
    // ---------------------------------------------------------
    // Содержит текст сообщения об ошибке для отображения пользователю
    data class Error(val message: String) : DatabaseExportProgress()
}