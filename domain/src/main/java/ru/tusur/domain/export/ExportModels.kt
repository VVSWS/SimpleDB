package ru.tusur.domain.export

import kotlinx.serialization.Serializable

// ---------------------------------------------------------
// DTO для экспорта одной записи о неисправности
// ---------------------------------------------------------
// Используется для сериализации в JSON при экспорте/импорте базы данных
// Содержит все поля записи в "плоском" виде (без вложенных объектов)
// Изображения экспортируются отдельными файлами, здесь хранятся только их URI
@Serializable
data class ExportEntry(
    // ---------------------------------------------------------
    // Идентификатор записи (сохраняется для восстановления связей)
    // ---------------------------------------------------------
    val id: Long,

    // ---------------------------------------------------------
    // Временная метка создания записи (Unix timestamp)
    // ---------------------------------------------------------
    val timestamp: Long,

    // ---------------------------------------------------------
    // Справочные данные (в виде примитивов)
    // ---------------------------------------------------------
    val year: Int?,           // Год выпуска (числовое значение)
    val brand: String?,       // Марка автомобиля (название)
    val modelName: String?,   // Название модели
    val location: String?,    // Местоположение (название)

    // ---------------------------------------------------------
    // Содержимое записи
    // ---------------------------------------------------------
    val title: String,        // Заголовок неисправности
    val description: String,  // Подробное описание

    // ---------------------------------------------------------
    // Список URI изображений (сами файлы экспортируются отдельно)
    // ---------------------------------------------------------
    val images: List<String>
)

// ---------------------------------------------------------
// Контейнер для экспорта всей базы данных
// ---------------------------------------------------------
// Содержит список всех экспортируемых записей
// Используется как корневой объект JSON-файла экспорта
@Serializable
data class ExportDatabase(
    val entries: List<ExportEntry>  // Список всех записей
)