package ru.tusur.domain.model

// ---------------------------------------------------------
// Основная доменная модель записи о неисправности
// ---------------------------------------------------------
// Представляет полную информацию о дефекте или поломке техники
// Используется во всех слоях приложения (данные, домен, презентация)
// Содержит справочные данные (год, марка, модель, локация) и контент (заголовок, описание)
data class FaultEntry(
    // ---------------------------------------------------------
    // Идентификатор записи
    // ---------------------------------------------------------
    // 0 означает новую запись (ID будет сгенерирован базой данных)
    val id: Long = 0L,

    // ---------------------------------------------------------
    // Справочные данные
    // ---------------------------------------------------------
    val year: Year? = null,      // Год выпуска техники (может отсутствовать)
    val brand: Brand? = null,    // Марка автомобиля (может отсутствовать)
    val model: Model? = null,    // Модель автомобиля (может отсутствовать)
    val location: Location? = null,  // Местоположение неисправности (может отсутствовать)

    // ---------------------------------------------------------
    // Содержимое записи
    // ---------------------------------------------------------
    val title: String = "",              // Заголовок/краткое описание проблемы
    val description: String = "",        // Подробное описание неисправности

    // ---------------------------------------------------------
    // Метаданные
    // ---------------------------------------------------------
    val timestamp: Long = 0L,            // Временная метка создания (Unix timestamp)

    // ---------------------------------------------------------
    // Изображения
    // ---------------------------------------------------------
    val imageUris: List<String> = emptyList()  // Список URI связанных изображений
)