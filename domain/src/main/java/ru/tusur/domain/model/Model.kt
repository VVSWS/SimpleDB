package ru.tusur.domain.model

// ---------------------------------------------------------
// Доменная модель модели автомобиля
// ---------------------------------------------------------
// Представляет конкретную модель техники (например, Camry, X5, Granta)
// Привязана к марке (Brand) и году выпуска (Year)
// Одна и та же модель может существовать у разных марок или в разные годы
data class Model(
    val name: String,   // Название модели (например, "Camry", "X5", "Granta")
    val brand: Brand,   // Марка, к которой относится модель (например, "Toyota")
    val year: Year      // Год выпуска, для которого доступна модель
) {
    // ---------------------------------------------------------
    // Переопределение toString для удобного отображения в UI
    // ---------------------------------------------------------
    // Возвращает название модели при преобразовании в строку
    // Упрощает использование в выпадающих списках и текстовых полях
    override fun toString() = name
}