package ru.tusur.domain.model

// ---------------------------------------------------------
// Модель фильтра поиска для записей о неисправностях
// ---------------------------------------------------------
// Инкапсулирует параметры фильтрации при поиске записей
// Все поля опциональны (null означает "не фильтровать по этому критерию")
// Используется в UseCase поиска и в UI для передачи выбранных фильтров
data class SearchFilter(
    val year: Int? = null,      // Год выпуска (если указан - только записи с этим годом)
    val brand: String? = null,  // Марка автомобиля (если указана - только записи с этой маркой)
    val model: String? = null,  // Модель автомобиля (если указана - только записи с этой моделью)
    val location: String? = null  // Местоположение (если указано - только записи с этой локацией)
)