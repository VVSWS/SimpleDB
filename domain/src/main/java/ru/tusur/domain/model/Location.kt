package ru.tusur.domain.model

// ---------------------------------------------------------
// Доменная модель местоположения неисправности
// ---------------------------------------------------------
// Представляет место возникновения проблемы (например, "Двигатель", "Ходовая", "Электрика")
// Используется для категоризации и фильтрации записей о неисправностях
data class Location(val name: String) {
    // ---------------------------------------------------------
    // Переопределение toString для удобного отображения в UI
    // ---------------------------------------------------------
    // Возвращает название локации при преобразовании в строку
    // Упрощает использование в выпадающих списках и текстовых полях
    override fun toString() = name
}