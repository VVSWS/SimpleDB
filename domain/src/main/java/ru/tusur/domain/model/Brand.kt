package ru.tusur.domain.model

// ---------------------------------------------------------
// Доменная модель марки автомобиля
// ---------------------------------------------------------
// Представляет производителя техники (например, Toyota, BMW, Lada)
// Используется в справочниках и связывается с моделями и записями
data class Brand(val name: String) {
    // ---------------------------------------------------------
    // Переопределение toString для удобного отображения в UI
    // ---------------------------------------------------------
    // Возвращает название марки при преобразовании в строку
    // Упрощает использование в текстовых компонентах Compose
    override fun toString() = name
}