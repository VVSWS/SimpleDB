package ru.tusur.domain.model

// ---------------------------------------------------------
// Доменная обёртка для записи с её изображениями
// ---------------------------------------------------------
// Объединяет запись о неисправности и список URI связанных с ней изображений
// Используется для передачи данных между слоями, когда требуется работа с изображениями
// (экспорт, импорт, детальный просмотр, аудиоплеер)
data class EntryWithImages(
    val entry: FaultEntry,        // Запись о неисправности (основные данные)
    val imageUris: List<String>   // Список URI изображений, привязанных к записи
)