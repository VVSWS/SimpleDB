package ru.tusur.domain.model

// ---------------------------------------------------------
// Доменная модель года выпуска техники
// ---------------------------------------------------------
// Представляет календарный год выпуска автомобиля (например, 2020, 2021)
// Используется в справочниках и связывается с моделями и записями
data class Year(val value: Int) {
    // ---------------------------------------------------------
    // Переопределение toString для удобного отображения в UI
    // ---------------------------------------------------------
    // Возвращает год в виде строки при преобразовании
    // Упрощает использование в текстовых компонентах Compose
    override fun toString() = value.toString()
}