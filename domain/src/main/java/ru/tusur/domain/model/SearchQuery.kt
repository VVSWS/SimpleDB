package ru.tusur.domain.model

// ---------------------------------------------------------
// Модель запроса поиска для слоя данных
// ---------------------------------------------------------
// Представляет параметры поиска в формате, понятном репозиторию
// Используется для передачи фильтров из UseCase в реализацию репозитория
// Отделена от SearchFilter для возможности расширения в будущем
data class SearchQuery(
    val year: Int?,        // Год выпуска (null - не фильтровать)
    val brand: String?,    // Марка автомобиля (null - не фильтровать)
    val model: String?,    // Модель автомобиля (null - не фильтровать)
    val location: String?  // Местоположение (null - не фильтровать)
) {
    // ---------------------------------------------------------
    // Флаг, указывающий, что запрос не содержит ни одного фильтра
    // ---------------------------------------------------------
    // true - все поля null (поиск без фильтрации)
    // false - хотя бы один фильтр указан
    val isEmpty: Boolean
        get() = year == null && brand == null && model == null && location == null
}