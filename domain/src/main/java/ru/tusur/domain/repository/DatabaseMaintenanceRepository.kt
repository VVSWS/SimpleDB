package ru.tusur.domain.repository

// ---------------------------------------------------------
// Интерфейс репозитория для обслуживания базы данных
// ---------------------------------------------------------
// Определяет операции по техническому обслуживанию базы данных
// Реализуется в слое данных (DatabaseMaintenanceRepositoryImpl)
// Позволяет доменному слою выполнять обслуживание БД без привязки к конкретной реализации
interface DatabaseMaintenanceRepository {

    // ---------------------------------------------------------
    // Выполнение команды VACUUM
    // ---------------------------------------------------------
    // VACUUM перестраивает всю базу данных, освобождая неиспользуемое пространство
    // Дефрагментирует файл БД, уменьшая его размер и улучшая производительность
    // Рекомендуется выполнять после массового удаления данных
    suspend fun vacuum()
}