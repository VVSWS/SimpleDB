package ru.tusur.domain.repository

import java.io.File

// ---------------------------------------------------------
// Интерфейс валидатора базы данных
// ---------------------------------------------------------
// Определяет контракт для проверки целостности и корректности файла базы данных
// Реализуется в слое данных (RoomDatabaseValidator)
// Используется перед переключением на новую БД для проверки её пригодности
interface DatabaseValidator {

    // ---------------------------------------------------------
    // Sealed class для представления результата валидации
    // ---------------------------------------------------------
    // Success - валидация пройдена успешно
    // Error - ошибка с описанием причины
    sealed class ValidationResult {
        object Success : ValidationResult()
        data class Error(val message: String) : ValidationResult()
    }

    // ---------------------------------------------------------
    // Валидация файла базы данных
    // ---------------------------------------------------------
    // dbFile: файл базы данных для проверки
    // Возвращает ValidationResult (Success или Error с описанием)
    suspend fun validateDatabase(dbFile: File): ValidationResult
}