package ru.tusur.core.util

import android.content.Context
import java.io.File
import java.io.IOException

// ---------------------------------------------------------
// Утилиты для работы с файлами (объект-синглтон)
// ---------------------------------------------------------
// Предоставляет методы для управления файлами базы данных
// Включает операции получения активной БД и копирования файлов
object FileHelper {

    // ---------------------------------------------------------
    // Константы для именования файлов базы данных
    // ---------------------------------------------------------
    // Префикс для всех файлов БД (BD_ = База Данных)
    private const val DB_PREFIX = "BD_"

    // Имя активной (текущей используемой) базы данных
    private const val ACTIVE_DB_NAME = "${DB_PREFIX}active.db"

    // ---------------------------------------------------------
    // Получение файла активной базы данных
    // ---------------------------------------------------------
    // Возвращает File, указывающий на БД в стандартной директории баз данных приложения
    // Путь: /data/data/<package_name>/databases/BD_active.db
    // Автоматически создаёт родительскую директорию, если она отсутствует
    fun getActiveDatabaseFile(context: Context): File {
        // Получение пути к БД через системный метод (Android автоматически добавляет директорию databases)
        val file = context.getDatabasePath(ACTIVE_DB_NAME)

        // Создание родительской директории (databases), если она не существует
        // Предотвращает ошибки при последующей записи файла
        file.parentFile?.mkdirs()

        return file
    }

    // ---------------------------------------------------------
    // Копирование файла с перезаписью целевого файла
    // ---------------------------------------------------------
    // Полностью копирует содержимое source в dest
    // Если dest существует - перезаписывает его
    // Аннотация @Throws(IOException::class) указывает, что метод может выбросить IOException
    @Throws(IOException::class)
    fun copyFile(source: File, dest: File) {
        // Создание родительской директории для целевого файла, если она не существует
        dest.parentFile?.mkdirs()

        // Копирование с использованием try-with-resources (use автоматически закрывает потоки)
        source.inputStream().use { input ->
            dest.outputStream().use { output ->
                // Побайтовое копирование из входного потока в выходной
                input.copyTo(output)
            }
        }
    }
}