package ru.tusur.core.model

// ---------------------------------------------------------
// Иерархия классов для представления ошибок (sealed class)
// ---------------------------------------------------------
// Используется в качестве типа возврата для операций, которые могут завершиться ошибкой
// Альтернатива выбрасыванию исключений (Result<Success, Failure>)
sealed class Failure {

    // ---------------------------------------------------------
    // Ошибка сети
    // ---------------------------------------------------------
    // Отсутствие интернет-соединения, таймаут, ошибка DNS
    object Network : Failure()

    // ---------------------------------------------------------
    // Ошибка данных
    // ---------------------------------------------------------
    // Некорректный формат данных, ошибка парсинга, пустой результат
    object Data : Failure()

    // ---------------------------------------------------------
    // Ошибка валидации
    // ---------------------------------------------------------
    // Входные данные не прошли проверку (пустое поле, некорректный формат)
    object Validation : Failure()

    // ---------------------------------------------------------
    // Неизвестная ошибка с сохранением исходного исключения
    // ---------------------------------------------------------
    // Любая ошибка, не подпадающая под категории выше
    // Содержит исходный Throwable для логирования и отладки
    data class Unknown(val error: Throwable) : Failure()

    // ---------------------------------------------------------
    // Фабричный метод для преобразования Throwable в Failure
    // ---------------------------------------------------------
    // Сопоставляет известные типы исключений с соответствующими Failure
    // Остальные исключения оборачиваются в Unknown
    companion object {
        fun from(error: Throwable): Failure = when (error) {
            // IllegalArgumentException часто возникает при валидации входных данных
            is IllegalArgumentException -> Validation
            // Все остальные исключения (IOException, NullPointerException и т.д.)
            else -> Unknown(error)
        }
    }
}