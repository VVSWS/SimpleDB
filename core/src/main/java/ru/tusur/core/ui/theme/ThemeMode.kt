package ru.tusur.core.ui.theme

// ---------------------------------------------------------
// Перечисление режимов темы оформления приложения
// ---------------------------------------------------------
// Определяет возможные варианты цветовой схемы интерфейса
// Используется в настройках и для сохранения выбора пользователя
enum class ThemeMode(val value: Int) {

    // ---------------------------------------------------------
    // Системная тема (следует за настройками ОС)
    // ---------------------------------------------------------
    // Применяется светлая тема, если в системе светлая,
    // и тёмная, если в системе тёмная тема
    SYSTEM(0),

    // ---------------------------------------------------------
    // Светлая тема (принудительно)
    // ---------------------------------------------------------
    // Всегда используется светлая цветовая схема,
    // независимо от настроек системы
    LIGHT(1),

    // ---------------------------------------------------------
    // Тёмная тема (принудительно)
    // ---------------------------------------------------------
    // Всегда используется тёмная цветовая схема,
    // независимо от настроек системы
    DARK(2);

    companion object {
        // ---------------------------------------------------------
        // Преобразование числового значения в элемент перечисления
        // ---------------------------------------------------------
        // Используется при чтении сохранённой темы из DataStore
        // Если значение не найдено, возвращается SYSTEM по умолчанию
        fun fromValue(value: Int): ThemeMode =
            entries.find { it.value == value } ?: SYSTEM
    }
}