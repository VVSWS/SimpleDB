package ru.tusur.presentation.shared

import androidx.lifecycle.ViewModel
import kotlinx.coroutines.flow.MutableSharedFlow
import kotlinx.coroutines.flow.SharedFlow

// ---------------------------------------------------------
// Общая ViewModel для глобальных событий приложения
// ---------------------------------------------------------
// Позволяет обмениваться событиями между различными экранами
// Живёт дольше отдельных экранов (область видимости - уровень приложения)
// Используется для уведомления MainViewModel об изменениях данных
class SharedAppEventsViewModel : ViewModel() {

    // ---------------------------------------------------------
    // Поток событий (SharedFlow с буфером на 1 событие)
    // ---------------------------------------------------------
    // extraBufferCapacity = 1 позволяет буферизировать одно событие
    // на случай, если коллектор ещё не готов
    private val _events = MutableSharedFlow<AppEvent>(extraBufferCapacity = 1)
    val events: SharedFlow<AppEvent> = _events

    // ---------------------------------------------------------
    // Отправка события в поток
    // ---------------------------------------------------------
    // Приостанавливающая функция (suspending), ожидает,
    // пока коллектор не будет готов принять событие (надёжная доставка)
    suspend fun emit(event: AppEvent) {
        _events.emit(event)
    }
}

// ---------------------------------------------------------
// Типы глобальных событий приложения
// ---------------------------------------------------------
sealed interface AppEvent {

    // ---------------------------------------------------------
    // Событие: создана новая база данных
    // ---------------------------------------------------------
    // Возникает при создании новой пустой БД
    data object DatabaseCreated : AppEvent

    // ---------------------------------------------------------
    // Событие: выполнено слияние с резервной БД
    // ---------------------------------------------------------
    // Возникает при успешном импорте данных из JSON
    data object DatabaseMerged : AppEvent

    // ---------------------------------------------------------
    // Событие: изменена запись (добавление, обновление, удаление)
    // ---------------------------------------------------------
    // Возникает при любом изменении данных о неисправностях
    data object EntryChanged : AppEvent
}