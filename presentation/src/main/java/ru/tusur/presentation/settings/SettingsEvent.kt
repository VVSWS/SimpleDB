package ru.tusur.presentation.settings

// ---------------------------------------------------------
// Sealed class для событий, возникающих на экране настроек
// ---------------------------------------------------------
// Используется для передачи информации из ViewModel в UI
// Позволяет обрабатывать различные события единообразно
sealed class SettingsEvent {

    // ---------------------------------------------------------
    // Событие: база данных успешно создана
    // ---------------------------------------------------------
    // Возникает после создания новой пустой базы данных
    // UI может использовать это для показа уведомления или закрытия диалога
    data object DatabaseCreated : SettingsEvent()

    // ---------------------------------------------------------
    // Событие: ошибка при работе с базой данных
    // ---------------------------------------------------------
    // Возникает при неудачном создании, импорте или слиянии БД
    // Содержит текст сообщения об ошибке для отображения пользователю
    data class DatabaseError(val message: String) : SettingsEvent()
}