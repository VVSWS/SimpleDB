package ru.tusur.presentation.util

import android.content.Context
import ru.tusur.presentation.R

// ---------------------------------------------------------
// Интерфейс провайдера строковых ресурсов
// ---------------------------------------------------------
// Абстракция для получения локализованных строк
// Позволяет тестировать презентационный слой без Android-контекста
// Реализации могут использовать Android Resources или моки для тестов
interface StringProvider {

    // ---------------------------------------------------------
    // Получение строки по ресурсному ID с форматированием
    // ---------------------------------------------------------
    // id: идентификатор строкового ресурса (R.string.*)
    // args: аргументы для форматирования (String.format)
    fun get(id: Int, vararg args: Any): String

    // ---------------------------------------------------------
    // Строка: "База данных создана"
    // ---------------------------------------------------------
    fun databaseCreated(): String

    // ---------------------------------------------------------
    // Строка: "Ошибка создания базы данных"
    // ---------------------------------------------------------
    fun databaseCreateError(): String
}

// ---------------------------------------------------------
// Реализация StringProvider для Android
// ---------------------------------------------------------
// Использует Context для доступа к ресурсам приложения
// Внедряется через DI (Koin) в презентационный слой
class AndroidStringProvider(
    private val context: Context  // Контекст для доступа к ресурсам
) : StringProvider {

    // ---------------------------------------------------------
    // Делегирование получения строки в Context
    // ---------------------------------------------------------
    override fun get(id: Int, vararg args: Any): String =
        context.getString(id, *args)

    // ---------------------------------------------------------
    // Возврат строки "База данных создана"
    // ---------------------------------------------------------
    override fun databaseCreated(): String =
        context.getString(R.string.db_created)

    // ---------------------------------------------------------
    // Возврат строки "Ошибка создания базы данных"
    // ---------------------------------------------------------
    override fun databaseCreateError(): String =
        context.getString(R.string.db_create_failed)
}