package ru.tusur.presentation.common

// ---------------------------------------------------------
// Перечисление возможных ошибок на экране списка записей
// ---------------------------------------------------------
// Используется в EntryListViewModel для передачи информации об ошибке в UI
// Позволяет отображать локализованные сообщения в зависимости от типа ошибки
enum class EntryListError {

    // ---------------------------------------------------------
    // Ошибка загрузки записей
    // ---------------------------------------------------------
    // Возникает при неудачном получении последних записей из репозитория
    // (проблемы с базой данных, отсутствие прав доступа и т.д.)
    LoadFailed,

    // ---------------------------------------------------------
    // Ошибка выполнения поиска
    // ---------------------------------------------------------
    // Возникает при неудачном выполнении поискового запроса
    SearchFailed
}